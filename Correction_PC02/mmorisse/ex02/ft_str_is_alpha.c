#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i])
	{
		if ((str[i] > 64 && str[i] < 91) || (str[i] > 96 && str[i] < 123))
			i++;
		else
			return (0);
	}
	return (1);
}

int	main(void)
{
	char	src[] = "HELlomyBoy";
	int		rtn;

	rtn = ft_str_is_alpha(src);
	printf("%d\n", rtn);
	return (0);
}

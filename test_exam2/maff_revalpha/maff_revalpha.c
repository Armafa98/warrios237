#include <unistd.h>

int	main(void)
{
	char	x;
	char	y;

	x = 'z';
	y = 'Y';
	while (x >= 'b' && y >= 'A')
	{
		write(1, &x, 1);
		write(1, &y, 1);
		x -= 2;
		y -= 2;
	}
	write(1, "\n", 1);
}
